# This script performs basic arithmetic operations: addition, subtraction, and multiplication.
# variables to hold the numbers
number1 = 10
number2 = 5

# addition operation
sum = number1 + number2
print(f"Addition of {number1} and {number2} is {sum} ")

# subtraction operation
difference = number1 - number2
print(f"Subtraction of {number1} and {number2} is {difference} ")

# multiplication operation
product = number1 * number2
print(f"Multiplication of {number1} and {number2} is {product} ")
